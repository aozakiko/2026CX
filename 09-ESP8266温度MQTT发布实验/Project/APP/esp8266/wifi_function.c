static void ESP8266_MQTT_Apply_Led ( char on )
{
	if ( on )
	{
		GPIO_ResetBits ( LED1_PORT, LED1_PIN );
		GPIO_ResetBits ( LED2_PORT, LED2_PIN );
		PC_Usart ( "\r\n>>> LED ON (DS0/DS1)\r\n" );
	}
	else
	{
		GPIO_SetBits ( LED1_PORT, LED1_PIN );
		GPIO_SetBits ( LED2_PORT, LED2_PIN );
		PC_Usart ( "\r\n>>> LED OFF\r\n" );
	}
}


static void ESP8266_MQTT_HandlePayload ( char * frame )
{
	char * head, * payload, * line2;
	char msg [32] = { 0 };
	int link_id, data_len;

	head = strstr ( frame, "+MQTTSUBRECV" );
	if ( head == NULL )
		return;

	PC_Usart ( "\r\n>>> MQTT RX:\r\n%s\r\n", frame );

	if ( sscanf ( head, "+MQTTSUBRECV:%d,\"%*[^\"]\",%d,%31s", &link_id, &data_len, msg ) >= 3 )
	{
		payload = msg;
	}
	else
	{
		payload = strrchr ( head, ',' );
		if ( payload == NULL )
			return;
		payload++;
		line2 = strchr ( head, '\n' );
		if ( line2 != NULL && ( payload [0] < '0' || payload [0] > '9' || strlen ( payload ) > 4 ) )
		{
			line2++;
			while ( *line2 == '\r' || *line2 == '\n' )
				line2++;
			if ( *line2 != '\0' )
				payload = line2;
		}
	}

	if ( strstr ( payload, "on" ) || strstr ( payload, "status: 1" ) || payload [0] == '1' )
		ESP8266_MQTT_Apply_Led ( 1 );
	else if ( strstr ( payload, "off" ) || strstr ( payload, "status: 0" ) || payload [0] == '0' )
		ESP8266_MQTT_Apply_Led ( 0 );
	else
		PC_Usart ( "\r\n>>> Unknown payload: [%s]\r\n", payload );
}


#define MQTT_TOPIC_TEMP   "stm32/temperature"


void ESP8266_MQTT_Publish_Temp ( void )
{
	char payload [48];
	float temp;

	temp = ADC_Temp_ReadCelsius();
	/* AT+MQTTPUB 的载荷不能含英文双引号 "，否则 AT 解析失败 */
	sprintf ( payload, "%.1f", temp );
	if ( ESP8266_Set_MQTT_Public ( MQTT_TOPIC_TEMP, payload ) )
		PC_Usart ( "\r\n>>> Published %s = %s C\r\n", MQTT_TOPIC_TEMP, payload );
	else
		PC_Usart ( "\r\n>>> Publish temp failed\r\n" );
}


void ESP8266_MQTT_Poll ( void )
{
	char * buf;

	if ( ! strEsp8266_Fram_Record .InfBit .FramFinishFlag )
		return;

	buf = strEsp8266_Fram_Record .Data_RX_BUF;
	buf [ strEsp8266_Fram_Record .InfBit .FramLength ] = '\0';

	if ( strstr ( buf, "+MQTTSUBRECV" ) != NULL )
		ESP8266_MQTT_HandlePayload ( buf );

	strEsp8266_Fram_Record .InfBit .FramLength = 0;
	strEsp8266_Fram_Record .InfBit .FramFinishFlag = 0;
}




