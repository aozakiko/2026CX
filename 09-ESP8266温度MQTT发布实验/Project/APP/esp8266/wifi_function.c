bool ESP8266_JoinAP ( char * pSSID, char * pPassWord )
{
	char cCmd [120];
	u32 elapsed;
	bool got_ip = false;

	ESP8266_Wait_Not_Busy ( 3000 );
	ESP8266_Cmd ( "AT+CWQAP", "OK", NULL, 2000 );
	delay_ms ( 800 );
	ESP8266_WiFi_Prep ();

	sprintf ( cCmd, "AT+CWJAP=\"%s\",\"%s\"", pSSID, pPassWord );

	strEsp8266_Fram_Record .InfBit .FramLength = 0;
	strEsp8266_Fram_Record .InfBit .FramFinishFlag = 0;
	ESP8266_Usart ( "%s\r\n", cCmd );

	for ( elapsed = 0; elapsed < 30000; elapsed += 200 )
	{
		delay_ms ( 200 );

		if ( ! strEsp8266_Fram_Record .InfBit .FramFinishFlag )
			continue;

		strEsp8266_Fram_Record .Data_RX_BUF [ strEsp8266_Fram_Record .InfBit .FramLength ] = '\0';
		PC_Usart ( "%s", strEsp8266_Fram_Record .Data_RX_BUF );

		if ( strstr ( strEsp8266_Fram_Record .Data_RX_BUF, "WIFI GOT IP" ) )
		{
			got_ip = true;
			break;
		}

		if ( strstr ( strEsp8266_Fram_Record .Data_RX_BUF, "FAIL" ) ||
		     strstr ( strEsp8266_Fram_Record .Data_RX_BUF, "+CWJAP:" ) )
		{
			ESP8266_Print_CWJAP_Error ( strEsp8266_Fram_Record .Data_RX_BUF );
			break;
		}

		strEsp8266_Fram_Record .InfBit .FramLength = 0;
		strEsp8266_Fram_Record .InfBit .FramFinishFlag = 0;
	}

	delay_ms ( 500 );
	return got_ip;
}


