static bool ESP8266_Wait_Not_Busy ( u32 timeout_ms )
{
	u32 elapsed;

	for ( elapsed = 0; elapsed < timeout_ms; elapsed += 300 )
	{
		if ( ESP8266_Cmd ( "AT", "OK", NULL, 300 ) )
			return true;
		delay_ms ( 300 );
	}
	return false;
}


bool ESP8266_Cmd ( char * cmd, char * reply1, char * reply2, u32 waittime )
{
	u8 retry;

	for ( retry = 0; retry < 5; retry++ )
	{
		strEsp8266_Fram_Record .InfBit .FramLength = 0;
		strEsp8266_Fram_Record .InfBit .FramFinishFlag = 0;

		ESP8266_Usart ( "%s\r\n", cmd );

		if ( ( reply1 == 0 ) && ( reply2 == 0 ) )
			return true;

		delay_ms ( waittime );

		strEsp8266_Fram_Record .Data_RX_BUF [ strEsp8266_Fram_Record .InfBit .FramLength ] = '\0';
		PC_Usart ( "%s", strEsp8266_Fram_Record .Data_RX_BUF );

		if ( strstr ( strEsp8266_Fram_Record .Data_RX_BUF, "busy" ) )
		{
			ESP8266_Wait_Not_Busy ( 3000 );
			continue;
		}

		if ( strstr ( strEsp8266_Fram_Record .Data_RX_BUF, "ERROR" ) )
			return false;

		if ( ( reply1 != 0 ) && ( reply2 != 0 ) )
			return ( ( bool ) strstr ( strEsp8266_Fram_Record .Data_RX_BUF, reply1 ) ||
			         ( bool ) strstr ( strEsp8266_Fram_Record .Data_RX_BUF, reply2 ) );

		if ( reply1 != 0 )
			return ( ( bool ) strstr ( strEsp8266_Fram_Record .Data_RX_BUF, reply1 ) );

		return ( ( bool ) strstr ( strEsp8266_Fram_Record .Data_RX_BUF, reply2 ) );
	}
	return false;
}


